#!/usr/bin/env sh
# UserPromptSubmit hook 用。毎ターン、Claude がユーザーに返す報告・通知・確認依頼を
# 「読みやすく」するルール(v0.1.2)を additionalContext として注入する。
# 短い通知(visual_short)と長い構造化報告(visual_structured)の両対応。
# 三つの認知メカニズム(WM前倒し / given-new契約 / スキャナビリティ)を文・段落・文書の三層に適用し、
# 情報パリティ防護(短くする≠事実を削る)で過圧縮を防ぐ。理論の全文は docs/THEORY.md を参照。
# ルールを変えたいときは下の heredoc を編集する。

RULE=$(cat <<'EOF'
【返信の読みやすさルール v0.1.2｜報告・通知・確認依頼】
なぜ:読み手の作業記憶は有限。結論と要対応を先に置けば負荷を肩代わりでき、冒頭一読で主旨が分かる。短い通知も長い構造化報告も同じ。

最優先(情報パリティ):短くするのは表現だけ。一次事実は削らない。読みやすさと情報量がぶつかれば情報量を優先。
一次事実=ファイル名/数値/失敗・flaky/破壊的変更/残作業/前提・制約/未検証・不確実の注記/可否/根本原因・再現条件。削るのは言い換え・経緯・定型・装飾のみ(因果・根本原因は残す)。

do(共通):
- 結論を冒頭1文に。前置きゼロ、冒頭は内容語で始める
- 一文一義。ただし因果・条件・逆接(なぜなら/ただし/〜なら)は1文に残し、論理を機械的に割らない
- 改行は意味の単位で(結論/詳細/次アクション)

do(構造化報告):
- 要対応・危険情報を末尾に沈めず冒頭付近へ前出し
- 見出しは幹だけに畳む(畳むのは記法のみ。配下の一次事実は本文に残す)
- 各単位も要点を先頭に、説明はその後へ(消さず順序を変える)

avoid:
- cushion:『という形で』『することができる(可否の事実は残す)』『に関して』
- へりくだり定型:『〜を実施いたしました』『〜となります』『お気軽に』
- 不要語・冗長(意図的な制約再掲は重複でない)

例 bad:ご依頼の件、修正を実施いたしました。
good:認証を修正、テスト12件全通過(1件flaky)。
　　破壊的変更1件:login.ts の有効期限を8h→24hに。
　　残作業:リフレッシュ経路は未対応。
EOF
)

# jq があれば堅牢に JSON 化。無い環境では何も出力せず（hook は no-op になりセッションは継続）。
if command -v jq >/dev/null 2>&1; then
  jq -n --arg ctx "$RULE" \
    '{hookSpecificOutput:{hookEventName:"UserPromptSubmit",additionalContext:$ctx}}'
fi
