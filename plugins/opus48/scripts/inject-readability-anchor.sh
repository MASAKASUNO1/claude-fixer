#!/usr/bin/env sh
# UserPromptSubmit hook 用。毎ターン、Claude がユーザーに返す文末の報告・通知・確認依頼を
# 「読みやすく」するルールを additionalContext として注入する。
# 基準は readability-essence の visual_short アンカー
#   （結論冒頭 / 先頭2語=内容語 / 改行=意味単位 / 前置きゼロ / 一文一義 / cushion・へりくだり定型句回避）。
# inject-report-style.sh の上位互換（へりくだり回避の意図を内包しつつ採点アンカーで強化）。
# ルールを変えたいときは下の heredoc を編集する。

RULE=$(cat <<'EOF'
【返信の読みやすさルール｜文末の報告・通知・確認依頼】
なぜ:読み手の作業記憶は有限。結論を先に置けば負荷を肩代わりでき、一読で意味が通り再読が要らない。出力直前に自己照合せよ。

do:
- 結論/フックを冒頭1文に。最初の改行までで主旨が分かる
- 先頭2語は内容語(挨拶・定型句で始めない)
- 一文一義。主張が2つなら文を割る
- 改行は意味の単位で(結論/詳細/次アクションを分ける)
- 前置きゼロ。本題から始める

avoid:
- cushion連結語:『という形で』『することができる』『に関して』『にあたって』
- へりくだり/硬い定型句:『〜を実施いたしました』『〜に寄与すると考えられます』『ご不明な点がございましたらお気軽に』
- 二重否定・意味重複・不要語

例(報告)
bad:ご依頼の件、修正を実施いたしました。
good:認証モジュールを修正、テストは全通過。
　　login.ts の有効期限チェックを直しました。

例(エラー)
bad:エラーが発生してしまったようです。
good:ビルド失敗。原因は依存のバージョン不整合です。
　　package.json を揃えれば直ります。

対象は読みやすさのみ。正しさ・情報量は別。
EOF
)

# jq があれば堅牢に JSON 化。無い環境では何も出力せず（hook は no-op になりセッションは継続）。
if command -v jq >/dev/null 2>&1; then
  jq -n --arg ctx "$RULE" \
    '{hookSpecificOutput:{hookEventName:"UserPromptSubmit",additionalContext:$ctx}}'
fi
